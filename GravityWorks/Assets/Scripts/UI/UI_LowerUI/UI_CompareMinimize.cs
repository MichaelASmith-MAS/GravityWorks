    // ------------------------------------------------------------------------------
    // FUNCTIONS
    // ------------------------------------------------------------------------------

    // ------------------------------------------------------------------------------
    // Function Name: 
    // Return types: 
    // Argument types: 
    // Author: 
    // Date: 
    // ------------------------------------------------------------------------------
    // Purpose: 
    // ------------------------------------------------------------------------------
    void Update()
    {      
        for (int i = 0; i < images.Length; i++)
        {
            if (UNA_StaticVariables.isFireRatePickedUp)
            {
                images[i].enabled = false;
            }
            else
            {
                images[i].enabled = true;
            }
        }
    }

} // End UI_CompareMinimize